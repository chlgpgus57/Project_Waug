package com.wg.p1.model;

public class NationVO {
	
	private int city_num;
	private String continents;
	private String nation;
	private String city_name;
	private String city_img;
	private String city_img2;
	private int city_rec;
	
	
	public int getCity_rec() {
		return city_rec;
	}
	public void setCity_rec(int city_rec) {
		this.city_rec = city_rec;
	}
	public int getCity_num() {
		return city_num;
	}
	public void setCity_num(int city_num) {
		this.city_num = city_num;
	}
	public String getContinents() {
		return continents;
	}
	public void setContinents(String continents) {
		this.continents = continents;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public String getCity_img() {
		return city_img;
	}
	public void setCity_img(String city_img) {
		this.city_img = city_img;
	}
	public String getCity_img2() {
		return city_img2;
	}
	public void setCity_img2(String city_img2) {
		this.city_img2 = city_img2;
	}
}
